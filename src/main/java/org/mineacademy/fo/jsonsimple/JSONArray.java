	/**
	 * Constructs a {@linkplain JSONArray} with the elements of the given array.
	 *
	 * @param array the array
	 * @param <T>   the array type
	 * @since 1.0.0
	 */
	public <T> JSONArray(T[] array) {

		for (final T element : array)
			this.add(element);
	}

