	/**
	 * Converts this {@linkplain JSONArray} to a XML string.
	 *
	 * @param rootName name of the root element
	 * @return this JSON array as a XML string
	 * @since 1.1.0
	 */
	public String toXML(String rootName) {

		final StringBuilder builder = new StringBuilder();
		builder.append('<');
		builder.append(rootName);
		builder.append(" length=");
		builder.append(this.size());
		builder.append('>');

		for (final Object element : this)
			if (element instanceof JSONObject)
				builder.append(((JSONObject) element).toXML("item"));
			else if (element instanceof JSONArray)
				builder.append(((JSONArray) element).toXML("item"));
			else {

				builder.append("<item>");

				if (element != null)
					builder.append(String.valueOf(element));

				builder.append("</item>");
			}

		builder.append("</");
		builder.append(rootName);
		builder.append('>');

		return builder.toString();
	}
